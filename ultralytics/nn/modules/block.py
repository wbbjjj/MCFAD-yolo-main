class AEF(nn.Module):
    """
    Aligned Enhancement Fusion (AEF)
    Args (YAML 写法见下):
        inc: [c_in1, c_in2] 由 parse_model 自动传入
        ouc: 输出通道
        use_dcn (bool): 是否用 DCNv2 做几何对齐
        ems_kernels (list[int]): EMSConvP 的多尺度核大小
        use_lawds (bool): 当两路特征分辨率不一致时是否用 LAWDS 对齐
        lawds_groups (int): LAWDS 分组
    """
    def __init__(self, inc, ouc, use_dcn=True, ems_kernels=(1,3,5,7),
                 use_lawds=False, lawds_groups=16):
        super().__init__()
        c1, c2 = inc if isinstance(inc, (list, tuple)) else (inc, inc)
        self.proj1 = Conv(c1, ouc, k=1, s=1)
        self.proj2 = Conv(c2, ouc, k=1, s=1)

        # 细节增强（多尺度）——若通道不够或不整除，则回退为 Identity
        def make_emsp_or_id(ch):
            ok = (ch % len(ems_kernels) == 0) and (ch // len(ems_kernels) >= 16)
            return EMSConvP(channel=ch, kernels=list(ems_kernels)) if ok else nn.Identity()
        self.enh1 = make_emsp_or_id(ouc)
        self.enh2 = make_emsp_or_id(ouc)

        # 几何对齐：默认用 DCNv2，关闭时用普通 3×3 depthwise 近似
        self.align2 = DCNv2(ouc, ouc, 3, 1) if use_dcn else Conv(ouc, ouc, k=3, s=1, g=ouc)

        # 门控加权：先 concat，再用深度可分离 3×3 生成 2×ouc 的权重并 Sigmoid
        self.gate_dw = Conv(2 * ouc, 2 * ouc, k=3, s=1, g=2 * ouc)  # depthwise
        self.gate_pw = Conv(2 * ouc, 2 * ouc, k=1, s=1, g=1)
        self.sigmoid = nn.Sigmoid()

        # 可学习通道系数（限制在 [-1, 1]）
        self.x1_param = nn.Parameter(torch.ones((1, ouc, 1, 1)) * 0.5, requires_grad=True)
        self.x2_param = nn.Parameter(torch.ones((1, ouc, 1, 1)) * 0.5, requires_grad=True)

        self.fuse = Conv(ouc, ouc, k=1, s=1)

        # 可选的分辨率对齐
        self.use_lawds = use_lawds
        self.lawds = LAWDS(ouc, group=lawds_groups) if use_lawds else None

    @staticmethod
    def _clamp_abs_(p, val=1.0):
        with torch.no_grad():
            sign = p.sign()
            p.abs_().clamp_(val)
            p.mul_(sign)

    def forward(self, x):
        # x: [x1, x2]
        self._clamp_abs_(self.x1_param.data, 1.0)
        self._clamp_abs_(self.x2_param.data, 1.0)

        x1, x2 = x
        x1, x2 = self.proj1(x1), self.proj2(x2)

        # 如果分辨率不一致，尝试用 LAWDS 对齐（仅当 use_lawds=True）
        if self.use_lawds and (x1.shape[-1] != x2.shape[-1] or x1.shape[-2] != x2.shape[-2]):
            if x1.shape[-1] == x2.shape[-1] * 2 and x1.shape[-2] == x2.shape[-2] * 2:
                x1 = self.lawds(x1)
            elif x2.shape[-1] == x1.shape[-1] * 2 and x2.shape[-2] == x1.shape[-2] * 2:
                x2 = self.lawds(x2)
            # 其他比例暂不处理，保持原状

        # 多尺度细节增强
        x1 = self.enh1(x1)
        x2 = self.enh2(x2)

        # 几何对齐（对第二路）
        x2 = self.align2(x2)

        # 门控加权
        g = self.sigmoid(self.gate_pw(self.gate_dw(torch.cat([x1, x2], dim=1))))
        g1, g2 = torch.chunk(g, 2, dim=1)

        y = x1 * g1 * self.x1_param + x2 * g2 * self.x2_param
        return self.fuse(y)


class DFL(nn.Module):
    """
    Integral module of Distribution Focal Loss (DFL).

    Proposed in Generalized Focal Loss https://ieeexplore.ieee.org/document/9792391
    """

    def __init__(self, c1=16):
        """Initialize a convolutional layer with a given number of input channels."""
        super().__init__()
        self.conv = nn.Conv2d(c1, 1, 1, bias=False).requires_grad_(False)
        x = torch.arange(c1, dtype=torch.float)
        self.conv.weight.data[:] = nn.Parameter(x.view(1, c1, 1, 1))
        self.c1 = c1

    def forward(self, x):
        """Applies a transformer layer on input tensor 'x' and returns a tensor."""
        b, _, a = x.shape  # batch, channels, anchors
        return self.conv(x.view(b, 4, self.c1, a).transpose(2, 1).softmax(1)).view(b, 4, a)
        # return self.conv(x.view(b, self.c1, 4, a).softmax(1)).view(b, 4, a)


class Proto(nn.Module):
    """YOLOv8 mask Proto module for segmentation models."""

    def __init__(self, c1, c_=256, c2=32):
        """
        Initializes the YOLOv8 mask Proto module with specified number of protos and masks.

        Input arguments are ch_in, number of protos, number of masks.
        """
        super().__init__()
        self.cv1 = Conv(c1, c_, k=3)
        self.upsample = nn.ConvTranspose2d(c_, c_, 2, 2, 0, bias=True)  # nn.Upsample(scale_factor=2, mode='nearest')
        self.cv2 = Conv(c_, c_, k=3)
        self.cv3 = Conv(c_, c2)

    def forward(self, x):
        """Performs a forward pass through layers using an upsampled input image."""
        return self.cv3(self.cv2(self.upsample(self.cv1(x))))


class HGStem(nn.Module):
    """
    StemBlock of PPHGNetV2 with 5 convolutions and one maxpool2d.

    https://github.com/PaddlePaddle/PaddleDetection/blob/develop/ppdet/modeling/backbones/hgnet_v2.py
    """

    def __init__(self, c1, cm, c2):
        """Initialize the SPP layer with input/output channels and specified kernel sizes for max pooling."""
        super().__init__()
        self.stem1 = Conv(c1, cm, 3, 2, act=nn.ReLU())
        self.stem2a = Conv(cm, cm // 2, 2, 1, 0, act=nn.ReLU())
        self.stem2b = Conv(cm // 2, cm, 2, 1, 0, act=nn.ReLU())
        self.stem3 = Conv(cm * 2, cm, 3, 2, act=nn.ReLU())
        self.stem4 = Conv(cm, c2, 1, 1, act=nn.ReLU())
        self.pool = nn.MaxPool2d(kernel_size=2, stride=1, padding=0, ceil_mode=True)

    def forward(self, x):
        """Forward pass of a PPHGNetV2 backbone layer."""
        x = self.stem1(x)
        x = F.pad(x, [0, 1, 0, 1])
        x2 = self.stem2a(x)
        x2 = F.pad(x2, [0, 1, 0, 1])
        x2 = self.stem2b(x2)
        x1 = self.pool(x)
        x = torch.cat([x1, x2], dim=1)
        x = self.stem3(x)
        x = self.stem4(x)
        return x


class HGBlock(nn.Module):
    """
    HG_Block of PPHGNetV2 with 2 convolutions and LightConv.

    https://github.com/PaddlePaddle/PaddleDetection/blob/develop/ppdet/modeling/backbones/hgnet_v2.py
    """

    def __init__(self, c1, cm, c2, k=3, n=6, lightconv=False, shortcut=False, act=nn.ReLU()):
        """Initializes a CSP Bottleneck with 1 convolution using specified input and output channels."""
        super().__init__()
        block = LightConv if lightconv else Conv
        self.m = nn.ModuleList(block(c1 if i == 0 else cm, cm, k=k, act=act) for i in range(n))
        self.sc = Conv(c1 + n * cm, c2 // 2, 1, 1, act=act)  # squeeze conv
        self.ec = Conv(c2 // 2, c2, 1, 1, act=act)  # excitation conv
        self.add = shortcut and c1 == c2

    def forward(self, x):
        """Forward pass of a PPHGNetV2 backbone layer."""
        y = [x]
        y.extend(m(y[-1]) for m in self.m)
        y = self.ec(self.sc(torch.cat(y, 1)))
        return y + x if self.add else y


class SPP(nn.Module):
    """Spatial Pyramid Pooling (SPP) layer https://arxiv.org/abs/1406.4729."""

    def __init__(self, c1, c2, k=(5, 9, 13)):
        """Initialize the SPP layer with input/output channels and pooling kernel sizes."""
        super().__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_ * (len(k) + 1), c2, 1, 1)
        self.m = nn.ModuleList([nn.MaxPool2d(kernel_size=x, stride=1, padding=x // 2) for x in k])

    def forward(self, x):
        """Forward pass of the SPP layer, performing spatial pyramid pooling."""
        x = self.cv1(x)
        return self.cv2(torch.cat([x] + [m(x) for m in self.m], 1))


class SPPF(nn.Module):
    """Spatial Pyramid Pooling - Fast (SPPF) layer for YOLOv5 by Glenn Jocher."""

    def __init__(self, c1, c2, k=5):
        """
        Initializes the SPPF layer with given input/output channels and kernel size.

        This module is equivalent to SPP(k=(5, 9, 13)).
        """
        super().__init__()
        c_ = c1 // 2  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c_ * 4, c2, 1, 1)
        self.m = nn.MaxPool2d(kernel_size=k, stride=1, padding=k // 2)

    def forward(self, x):
        """Forward pass through Ghost Convolution block."""
        y = [self.cv1(x)]
        y.extend(self.m(y[-1]) for _ in range(3))
        return self.cv2(torch.cat(y, 1))
