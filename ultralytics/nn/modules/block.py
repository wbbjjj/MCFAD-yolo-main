class AGDDown(nn.Module):
    def __init__(self, c_in: int, c_out: int, k: int = 3, reduction: int = 4):
        super().__init__()
        assert k in (3, 5), "AGDDown 目前只设计支持 3 或 5 这类小卷积核"

        # 路径1：卷积下采样
        self.conv_down = Conv(c_in, c_out, k=k, s=2)
        # 路径2：池化下采样
        self.pool = nn.AvgPool2d(2, 2)
        self.pool_proj = Conv(c_in, c_out, k=1)
        # 门控网络
        gate_mid = max(c_out // reduction, 1)
        self.gate = nn.Sequential(
            Conv(c_out * 2, gate_mid, k=1),
            nn.SiLU(inplace=True),
            nn.Conv2d(gate_mid, 2, kernel_size=1, bias=True),  # 输出2个通道，对应两条路径
        )
        self.softmax = nn.Softmax(dim=1)
        # 残差缩放
        self.gamma = nn.Parameter(torch.ones(1))

    def forward(self, x):
        y_conv = self.conv_down(x)           # [B, C_out, H/2, W/2]
        y_pool = self.pool(x)               # [B, C_in, H/2, W/2]
        y_pool = self.pool_proj(y_pool)     # [B, C_out, H/2, W/2]

        # 拼接做门控
        y_cat = torch.cat([y_conv, y_pool], dim=1)
        gate_logits = self.gate(y_cat)
        gate = self.softmax(gate_logits)

        w_conv = gate[:, 0:1, :, :]
        w_pool = gate[:, 1:2, :, :]

        # 像素级别的自适应融合
        y = w_conv * y_conv + w_pool * y_pool

        # 残差缩放
        return self.gamma * y
