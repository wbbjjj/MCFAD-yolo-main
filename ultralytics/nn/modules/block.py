class Partial_conv3(nn.Module):
    def __init__(self, dim, n_div, forward):
        super().__init__()
        self.dim_conv3 = dim // n_div
        self.dim_untouched = dim - self.dim_conv3
        self.partial_conv3 = nn.Conv2d(self.dim_conv3, self.dim_conv3, 3, 1, 1, bias=False)

        if forward == 'slicing':
            self.forward = self.forward_slicing
        elif forward == 'split_cat':
            self.forward = self.forward_split_cat
        else:
            raise NotImplementedError

    def forward_slicing(self, x):
        # only for inference
        x = x.clone()   # !!! Keep the original input intact for the residual connection later
        x[:, :self.dim_conv3, :, :] = self.partial_conv3(x[:, :self.dim_conv3, :, :])
        return x

    def forward_split_cat(self, x):
        # for training/inference
        # x1, x2 = torch.split(x, [self.dim_conv3, self.dim_untouched], dim=1)
        x1, x2 = torch.split(x, [self.partial_conv3.weight.size(1), x.size(1) - self.partial_conv3.weight.size(1)], dim=1)
        x1 = self.partial_conv3(x1)
        x = torch.cat((x1, x2), 1)
        return x

class SCEMFusion(nn.Module):
    def __init__(self, channels, reduction=16, num_branches=2):
        super().__init__()
        self.channels = channels
        self.num_branches = num_branches
        hidden = max(channels // reduction, 1)

        self.avg = nn.AdaptiveAvgPool2d(1)
        self.fc1 = nn.Conv2d(channels, hidden, 1, 1, 0, bias=False)
        self.act = nn.SiLU()
        self.fc2 = nn.Conv2d(hidden, channels * num_branches, 1, 1, 0, bias=False)
        self.softmax = nn.Softmax(dim=1)

    def forward(self, xs):
        assert len(xs) == self.num_branches
        u = 0
        for x in xs:
            u = u + x
        s = self.avg(u)
        z = self.act(self.fc1(s))
        a_b = self.fc2(z)

        B, _, _, _ = a_b.shape
        a_b = a_b.view(B, self.num_branches, self.channels, 1, 1)
        a_b = self.softmax(a_b)

        out = 0
        for i, x in enumerate(xs):
            out = out + a_b[:, i] * x
        return out


class SCEMBlock(nn.Module):
    def __init__(self,
                 c1, c2,
                 shortcut=True,
                 e=0.5,
                 pooling_r=4,
                 ems_kernels=(1, 3, 5, 7),
                 fusion_reduction=16,
                 se_ratio=0.25,
                 pconv_n_div=4):
        super().__init__()
        self.add = shortcut and (c1 == c2)
        c_ = int(c2 * e)
        self.c_ = c_

        # 1x1 降维
        self.cv1 = Conv(c1, c_, k=1, s=1)

        self.sc = SCConv(c_, c_, s=1, g=1, pooling_r=pooling_r)
        ks = list(ems_kernels)
        while ks and (c_ // len(ks) < 16):
            ks = ks[:-1]

        if not ks or c_ < 16:
            self.ems = Conv(c_, c_, k=3, s=1)
        else:
            self.ems = EMSConvP(channel=c_, kernels=ks)

        self.fusion = SCEMFusion(channels=c_, reduction=fusion_reduction, num_branches=2)
        self.pconv = Partial_conv3(dim=c_, n_div=pconv_n_div, forward='split_cat')

        # 轻量通道注意力
        self.se = SELite(c_, r=se_ratio)

        self.cv2 = Conv(c_, c2, k=1, s=1)

    def forward(self, x):
        y = self.cv1(x)
        y_sc = self.sc(y)
        y_ms = self.ems(y)
        y = self.fusion([y_sc, y_ms])
        y = self.pconv(y)
        y = self.se(y)
        y = self.cv2(y)
        return x + y if self.add else y

